# --- التبويب الأول: عرض الوصفات والحسابات الرياضية ---
with tabs[0]:
    search = st.text_input("🔍 ابحث في الـ 100 وصفة المعتمدة:")
    filtered = [r for r in all_recipes if search.lower() in r['name'].lower() or search.lower() in r['cat'].lower()]
    
    for r in filtered:
        with st.expander(f"✨ {r['name']} - [{r['cat']}]"):
            col1, col2 = st.columns([2, 1])
            with col1:
                st.markdown(f"**المكونات الأساسية:**\n{r['ing']}")
                st.info(f"**طريقة العمل:**\n{r['method']}")
            with col2:
                st.markdown("**الأوزان المحسوبة:**")
                parts = r['ing'].split(',')
                calc_data = []
                for p in parts:
                    match = re.search(r'(\d+)%', p)
                    if match:
                        perc = int(match.group(1))
                        weight = (perc / 100) * total_w
                        calc_data.append({"المادة": p.split(str(perc))[0].strip(), "الوزن (g)": f"{weight:,.2f}"})
                if calc_data: st.table(pd.DataFrame(calc_data))

