def run_calc(ing_str, target):
    items = ing_str.split(',')
    res = []
    for i in items:
        match = re.search(r'(\d+)%', i)
        if match:
            p = int(match.group(1))
            w = (p/100) * target
            name = i.split(str(p))[0].strip()
            res.append({"المادة": name, "النسبة": f"{p}%", "الوزن (g)": f"{w:,.1f}"})
    return res

