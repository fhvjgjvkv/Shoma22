# 5. دالة معالجة الأوزان
def run_calc(ing_str, target):
    items = ing_str.split(',')
    results = []
    for i in items:
        match = re.search(r'(\d+)%', i)
        if match:
            percent = int(match.group(1))
            weight = (percent / 100) * target
            name = i.split(str(percent))[0].strip()
            results.append({"المادة": name, "النسبة": f"{percent}%", "الوزن المطلوب (g)": f"{weight:,.1f}"})
    return results

