def display_recipes(category_name, search_term):
    # تصفية الوصفات بناءً على القسم والبحث
    filtered = [r for r in recipes if r['cat'] == category_name]
    if search_term:
        filtered = [r for r in filtered if search_term.lower() in r['name'].lower() or search_term.lower() in r['ing'].lower()]
    
    if filtered:
        for r in filtered:
            with st.expander(f"⭐ {r['name']}"):
                st.markdown(f"**📝 المكونات:**\n{r['ing']}")
                st.info(f"**👨‍🔬 الطريقة:**\n{r['method']}")
    else:
        st.write("لا توجد وصفات في هذا القسم حالياً.")

